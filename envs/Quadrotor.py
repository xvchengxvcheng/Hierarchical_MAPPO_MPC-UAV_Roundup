    import numpy as np
    import matplotlib.pyplot as plt
    from mpl_toolkits.mplot3d.art3d import Line3DCollection

    Quad = Quadrotor()
    scan_N = 48
    scan_R = 30.0

    # laser: (N,3) 方向向量（Fibonacci 球面，通常已单位化；若不确定，下面会再单位化）
    laser = np.asarray(Quad._fibonacci_sphere(scan_N), dtype=float)
